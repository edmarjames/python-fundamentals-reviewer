# Create a program that:

# Declares two integer variables, one float, and one complex number.
# Perform an addition, subtraction, multiplication, and division between them.
# Print the results of each operation and their types using type().

x = 6.9
y = 1j

sum_of = x + y
print(sum_of)
print(type(sum_of))

difference = x - y
print(difference)
print(type(difference))

product = x * y
print(product)
print(type(product))

quotient = x / y
print(quotient)
print(type(quotient))